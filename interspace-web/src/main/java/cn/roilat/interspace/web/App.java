package cn.roilat.interspace.web;

public class App {
	public void demo() {
		float i = 1.0f;
		float j = 1.0f;
		System.out.println(i * j);
		System.out.println("hello");
	}

	public static void main(String[] args) {
		double i = 2.00f;
		double j = 1.10f;
		System.out.println(i - j);
		System.out.println(2.00d - 1.10d);
		System.out.println(2.00 - 1.10);
		System.out.println(2.00 - 1.10);
		System.out.println("hello");
	}
}
